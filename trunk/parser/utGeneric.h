#ifndef _UT_GENERIC_H
#define _UT_GENERIC_H

namespace utils
{
  template<typename T>
  class scoped_setter
  {
  public:

    scoped_setter(T &ref, T new_val)
      :m_ref(ref)
      ,m_buf(ref)
    {
      ref = new_val;
    }

   ~scoped_setter()
    {
      m_ref = m_buf;
    }

  private:
    T &m_ref;
    T  m_buf;
  };
}

#endif